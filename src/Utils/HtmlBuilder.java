    private static String generateTableReportsPercentage(List<String[]> data) {

        String r, porcentaje, cabecera;
        float s = 0.0f;
        String esp = "%20";
        // r =
        // "http://chart.apis.google.com/chart?chs=400x100&cht=p&chd=t:45,25,10,20&chl=Valencia|Madrid|Barcelona|Lugo\"
        // width=\"400\" height=\"100";
        // r =
        // "https://chart.apis.google.com/chart?chs=700x300&cht=p&chd=t:45,25,10,20&chl=Valencia|Madrid|Barcelona|Lugo%22%20width=%22400%22%20height=%22100%22";
        if (data.size() >= 2) {
            for (String[] fil : data) {
                // System.out.println(fil[1]);
                s = s + Float.parseFloat(fil[2]);
            }
        }
        porcentaje = "";
        cabecera = "";
        r = "";
        if (s > 0) {
            r = "https://chart.apis.google.com/chart?chs=700x300&cht=p&chd=t:";
            for (String[] d : data) {
                double f = (Float.parseFloat(d[2]) * 100 / (float) s);
                f = Math.round(f * 10d) / 10d;
                cabecera += f + "%" + " - " + d[1] + "|";
                porcentaje += f + ",";
            }
            porcentaje = porcentaje.substring(0, porcentaje.length() - 1);
            cabecera = cabecera.substring(0, cabecera.length() - 1);
            r = r + porcentaje + "&chl=" + cabecera;
            // System.out.println(r);
        }

        return r;
    }

